module sqrt_comb32(input [31:0] a, output [15:0] root, output [16:0] remainder, output valid, output busy, output done);
    // define a input 80.160.255   // define root output 120.255.160   // define remainder output 120.255.160
    // define valid output 255.255.255
    wire [15:0] rt; wire [16:0] rm;
    sqcore32 c(.a(a), .root(rt), .rem(rm));
    assign root = rt; assign remainder = rm;
    assign valid=1'b1; assign busy=1'b0; assign done=1'b1;
endmodule


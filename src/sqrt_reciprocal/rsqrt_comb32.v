module rsqrt_comb32(input [31:0] a, output [31:0] result, output valid);
    // define a input 80.160.255   // define result output 120.255.160   // define valid output 255.255.255
    // 1/sqrt(a) scaled: (2^32-1) / floor(sqrt(a)), fully structural
    wire [15:0] rt; wire [16:0] rm;
    rsqcore32 sc(.a(a), .root(rt), .rem(rm));
    wire [31:0] rt_ext = {{16{1'b0}}, rt};
    wire azero = ~(|a);
    wire [31:0] num = {32{1'b1}};   // 2^32-1 numerator
    wire [31:0] q, r; wire rd0,rov,rv,rb,rdn;
    rsqdiv32 dv(.a(num), .b(rt_ext), .quotient(q), .remainder(r),
        .divide_by_zero(rd0), .overflow(rov), .valid(rv), .busy(rb), .done(rdn));
    assign result = azero ? {32{1'b1}} : q;
    assign valid = ~azero;
endmodule


module mod_signed32(input signed [31:0] a, input signed [31:0] b, output signed [31:0] remainder, output divide_by_zero, output valid, output busy, output done);
    // define a input 80.160.255   // define b input 80.200.255   // define remainder output 120.255.160
    wire dz=~(|b);
    wire [31:0] q_unused, r;
    wire d2,o2,v2,b2,dn2;
    div_sgncore32 dv(.a(a),.b(b),.quotient(q_unused),.remainder(r),.divide_by_zero(d2),.overflow(o2),.valid(v2),.busy(b2),.done(dn2));
    assign remainder = dz ? a : r;
    assign divide_by_zero=dz; assign valid=~dz; assign busy=1'b0; assign done=1'b1;
endmodule

module div_sgncore32(input signed [31:0] a, input signed [31:0] b,
    output signed [31:0] quotient, output signed [31:0] remainder,
    output divide_by_zero, output overflow, output valid, output busy, output done);
    wire dz = ~(|b);
    wire sa=a[31], sb=b[31];
    wire [31:0] mag_a, mag_b; wire ca,cb;
    wire [31:0] ai = a ^ {32{sa}}, bi = b ^ {32{sb}};
    cincd32 na(.a(ai),.add(sa),.y(mag_a),.cout(ca));
    cincd32 nb(.a(bi),.add(sb),.y(mag_b),.cout(cb));
    wire [31:0] uq, ur; wire udz,uov,uv,ub,ud;
    udivcore32 dv(.a(mag_a),.b(mag_b),.quotient(uq),.remainder(ur),
        .divide_by_zero(udz),.overflow(uov),.valid(uv),.busy(ub),.done(ud));
    wire qs = sa ^ sb;
    wire [31:0] uq_i = uq ^ {32{qs}}; wire [31:0] q_fixed; wire qc;
    cincd32 fq(.a(uq_i),.add(qs),.y(q_fixed),.cout(qc));
    wire [31:0] ur_i = ur ^ {32{sa}}; wire [31:0] r_fixed; wire rc;
    cincd32 fr(.a(ur_i),.add(sa),.y(r_fixed),.cout(rc));
    assign quotient  = dz ? {32{1'b1}} : q_fixed;
    assign remainder = dz ? a : r_fixed;
    assign overflow = 1'b0; assign valid=~dz; assign busy=1'b0; assign done=1'b1;
endmodule


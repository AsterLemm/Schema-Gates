module multiply_accumulate_signed16(input clk, input rst, input en, input signed [15:0] a, input signed [15:0] b, output reg signed [35:0] acc);
    // define clk input 255.230.80   // define rst input 255.80.80   // define en input 255.180.80
    // define a input 80.160.255   // define b input 80.200.255   // define acc output 120.255.160
    wire signed [31:0] prod;
    smacmul16 mm(.a(a),.b(b),.product(prod));
    wire [35:0] prod_ext = {{4{prod[31]}}, prod};
    wire [35:0] sum_next; wire co;
    rca36 ac(.a(acc), .b(prod_ext), .cin(1'b0), .sum(sum_next), .cout(co));
    always @(posedge clk) if (rst) acc<=0; else if (en) acc<=sum_next;
endmodule

module smacmul16(input signed [15:0] a, input signed [15:0] b, output signed [31:0] product);
    wire sa=a[15], sb=b[15];
    wire [15:0] a_inv = a ^ {16{sa}};
    wire [15:0] b_inv = b ^ {16{sb}};
    wire [15:0] mag_a, mag_b; wire cao, cbo;
    cinc16 ia(.a(a_inv),.add(sa),.y(mag_a),.cout(cao));
    cinc16 ib(.a(b_inv),.add(sb),.y(mag_b),.cout(cbo));
    wire [31:0] umag;
    umulcore16 um(.a(mag_a),.b(mag_b),.product(umag));
    wire psign = sa ^ sb;
    wire [31:0] p_inv = umag ^ {32{psign}};
    wire [31:0] pneg; wire pco;
    cinc32 ip(.a(p_inv),.add(psign),.y(pneg),.cout(pco));
    assign product = pneg;
endmodule

